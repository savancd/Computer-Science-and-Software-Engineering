#   You define a function using the def keyword followed 
#   by the function name and a set of parentheses that may contain parameters. 

def greet(name):
    print("Hello, " + name + "!")

#   This function takes a name parameter and prints a greeting message to the console.
#   To call this function, you simply need to pass in an argument for the name parameter:

