#   Modifying Elements of a List Lists in Python are mutable, which
#   means you can modify individual elements of a list. To modify an
#   element, simply assign a new value to it using its index. 

fruits = ["Apple", "Banana", "Orange", "Kivi", "Grape"]

#   Change the second element of our fruits list to "pineapple":

fruits[1] = "Pineapple"
print(fruits)





