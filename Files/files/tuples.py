#   To create a tuple, enclose it in a comma-separated sequence of items in parentheses

my_tuple=(1,2,3,"four",5.0)

#   You can access individual elements in a tuple using indexing, just like with lists.
#   The first element in a tuple has an index of 0, the second element has an index of 1, and so on.

print(my_tuple[3])

