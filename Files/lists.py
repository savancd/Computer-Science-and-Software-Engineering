#   To create a list in Python, you can simply enclose a 
#   comma-separated sequence of items in square brackets

fruits = ["Apple", "Banana", "Orange", "Kivi", "Grape"]

#   You can access individual elements
#   of a list by their index. In Python, indexing starts at 0, so the
#   first element of the list has an index of 0, the second has an 
#   index of 1, and so on. To access an element, simply use its index
#   in square brackets after the list name. 

print(fruits[3])