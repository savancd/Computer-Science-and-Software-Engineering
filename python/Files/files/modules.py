#   To import a module, you use the import keyword followed by the module name. Here's an example:

import math

print(math.pi)

#   This will import the math module and print the value of pi to the console.
#   You can also import specific functions or variables from a module using the from keyword. 

from math import sqrt
print(sqrt(4))

#   This will import the sqrt function from the math module and print the square root of 4 to the console.

